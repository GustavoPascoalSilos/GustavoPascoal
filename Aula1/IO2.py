numeroUm = float(input("Digite um número: "))
numeroDois = float(input("Digite outro numero: "))

#Realização dos calculos
adicao = numeroUm + numeroDois
subtracao = numeroUm - numeroDois
multiplicacao = numeroUm * numeroDois
divisao = numeroUm / numeroDois

#Impressão dos resultados
print(f"O resultado da soma dos números é, {adicao}")
print(f"O resultado da subtração dos números é, {subtracao}")
print(f"O resultado da multiplicação dos números é, {multiplicacao}")
print(f"O resultado da divisão dos números é, {divisao}")
